def sum_two_numbers(a, b):
    """
    Calculates and returns the sum of two numbers that may be either integer
    or real in POOSL.
    """
    return a + b

def sort(list):
    """
    Sorts the POOSL array provided as an argument and returns it.
    """
    return sorted(list)

